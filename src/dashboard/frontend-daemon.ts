export interface FrontendDaemonOptions {
  ipcSocketPath: string;
  port: number;
  token: string;
}

export interface FrontendDaemonHandle {
  port: number;
  ipcSocketPath: string;
  registry: FrontendIpcServer;
  close(): Promise<void>;
}

export async function createFrontendDaemon(opts: FrontendDaemonOptions): Promise<FrontendDaemonHandle> {
  const { ipcSocketPath, token } = opts;

  // Start the IPC server (engine registry)
  const registry = new FrontendIpcServer(ipcSocketPath);
  await registry.listen();

  // Per-repo PTY bridge state. Each attached engine that exposes a bridge
  // gets its own entry with a pool of per-chat states.
  interface ChatEntry {
    ring: Buffer;
    tool: ToolKind;
    kind: "main" | "sidechat";
    clients: Set<WebSocket>;
  }
  interface PendingCreate {
    resolve: (chatId: string, tool: ToolKind) => void;
    reject: (error: string) => void;
  }
  interface PendingClose {
    resolve: () => void;
    reject: (error: string) => void;
  }
  interface RepoBridgeEntry {
    bridge: PtyBridge;
    chats: Map<string, ChatEntry>;
    pendingCreates: Map<string, PendingCreate>;
    pendingCloses: Map<string, PendingClose>;
  }
  const bridges = new Map<string, RepoBridgeEntry>();
  // WebSocket clients are tracked in per-chat Sets inside RepoBridgeEntry.chats.
  // wsToRepo/wsToChat let the upgrade handler route new connections.
  const wsToRepo = new WeakMap<WebSocket, string>();
  const wsToChat = new WeakMap<WebSocket, string>();

  function onEngineAttached(conn: EngineConnection): void {
    if (conn.repoPath) pruneOldPastedImages(conn.repoPath);
    if (!conn.ptyBridgePath || bridges.has(conn.repoId)) return;
    connectPtyBridge(conn.ptyBridgePath).then((bridge) => {
      const chatMap = new Map<string, ChatEntry>();
      const pendingCreates = new Map<string, PendingCreate>();
      const pendingCloses = new Map<string, PendingClose>();
      const entry: RepoBridgeEntry = { bridge, chats: chatMap, pendingCreates, pendingCloses };
      bridges.set(conn.repoId, entry);

      bridge.onChatList((chatList) => {
        // Sync per-chat state with the engine's authoritative list.
        const newIds = new Set(chatList.map(c => c.chatId));
        for (const cId of chatMap.keys()) {
          if (!newIds.has(cId)) chatMap.delete(cId);
        }
        for (const { chatId, kind, tool } of chatList) {
          if (!chatMap.has(chatId)) {
            chatMap.set(chatId, { ring: Buffer.alloc(0), tool, kind: kind as "main" | "sidechat", clients: new Set() });
          }
        }
        const mainChat = chatList.find(c => c.chatId === "main");
        if (mainChat) registry.updateChatTool(conn.repoId, mainChat.tool as ToolKind);
      });

      bridge.onChatData((chatId, chunk) => {
        const chat = chatMap.get(chatId);
        if (!chat) return;
        chat.ring = appendRing(chat.ring, chunk, RING_BUFFER_BYTES);
        const payload = JSON.stringify({ type: "data", bytes: chunk.toString("base64") });
        for (const ws of chat.clients) {
          if (ws.readyState === ws.OPEN) ws.send(payload);
        }
      });

      bridge.onChatTool((chatId, tool) => {
        const chat = chatMap.get(chatId);
        if (chat) {
          // On tool switch the engine clears its ring; mirror that here.
          chat.ring = Buffer.alloc(0);
          chat.tool = tool;
        }
        if (chatId === "main") {
          registry.updateChatTool(conn.repoId, tool);
          const mainChat = chatMap.get("main");
          if (mainChat) {
            const payload = JSON.stringify({ type: "chat_tool", tool });
            for (const ws of mainChat.clients) {
              if (ws.readyState === ws.OPEN) ws.send(payload);
            }
          }
        }
      });

      bridge.onChatCreated((clientReqId, chatId, tool) => {
        chatMap.set(chatId, { ring: Buffer.alloc(0), tool, kind: "sidechat", clients: new Set() });
        const pending = pendingCreates.get(clientReqId);
        if (pending) {
          pendingCreates.delete(clientReqId);
          pending.resolve(chatId, tool);
        }
      });

      bridge.onChatCreateFailed((clientReqId, error) => {
        const pending = pendingCreates.get(clientReqId);
        if (pending) {
          pendingCreates.delete(clientReqId);
          pending.reject(error);
        }
      });

      bridge.onChatClosed((chatId, clientReqId) => {
        const chat = chatMap.get(chatId);
        if (chat) {
          const payload = JSON.stringify({ type: "chat_ended" });
          for (const ws of chat.clients) {
            if (ws.readyState === ws.OPEN) {
              try { ws.send(payload); } catch { /* ignore */ }
              try { ws.close(); } catch { /* ignore */ }
            }
          }
          chatMap.delete(chatId);
        }
        if (clientReqId) {
          const pending = pendingCloses.get(clientReqId);
          if (pending) {
            pendingCloses.delete(clientReqId);
            pending.resolve();
          }
        }
      });

      bridge.onChatCloseFailed((clientReqId, error) => {
        const pending = pendingCloses.get(clientReqId);
        if (pending) {
          pendingCloses.delete(clientReqId);
          pending.reject(error);
        }
      });

    }).catch(() => { /* PTY bridge unavailable — engine without chat */ });
  }

  registry.on("engineAttached", onEngineAttached);
  registry.on("engineDetached", (repoId: string) => {
    const entry = bridges.get(repoId);
    if (!entry) return;
    try { entry.bridge.close(); } catch { /* ignore */ }
    bridges.delete(repoId);
  });

  // HTTP server
  const server = createServer((req, res) => {
    const url = req.url ?? "/";
    const pathname = url.split("?")[0];

    const cookieOk = getCookie(req, "sf_token") === token;
    const queryOk = getQueryToken(url) === token;
    if (!cookieOk && !queryOk) {
      res.writeHead(401, { "content-type": "text/plain" });
      res.end("unauthorized");
      return;
    }
    if (!cookieOk && queryOk && pathname === "/") {
      res.writeHead(302, {
        "set-cookie": `sf_token=${token}; Path=/; HttpOnly; SameSite=Strict`,
        location: "/",
      });
      res.end();
      return;
    }

    // Static files
    if (pathname === "/" || pathname === "/index.html") {
      const file = resolveStatic("index.html");
      if (file) return serveFile(res, file);
    }
    if (pathname.startsWith("/static/")) {
      const file = resolveStatic(pathname.slice("/static/".length));
      if (file) return serveFile(res, file);
      res.writeHead(404, { "content-type": "text/plain" });
      res.end("not found");
      return;
    }

    // SSE: fan out all engines' jobs
    if (pathname === "/events") {
      res.writeHead(200, {
        "content-type": "text/event-stream",
        "cache-control": "no-cache",
        connection: "keep-alive",
      });
      const send = () => {
        try {
          const jobs = registry.getAllJobs();
          const repos = registry.getRepos();
          res.write(`data: ${JSON.stringify({ jobs, repos })}\n\n`);
        } catch { /* client gone */ }
      };
      send();
      const unsubscribe = registry.onUpdate(send);
      const heartbeat = setInterval(() => {
        try { res.write(": ping\n\n"); } catch { /* ignore */ }
      }, 15_000);
      req.on("close", () => {
        clearInterval(heartbeat);
        unsubscribe();
      });
      return;
    }

    // GET /repos — list attached repos
    if (pathname === "/repos" && req.method === "GET") {
      return sendJson(res, 200, { repos: registry.getRepos() });
    }

    // Per-repo job routes: /repos/:repoId/jobs/:jobId/action
    const repoJobMatch = pathname.match(
      /^\/repos\/([A-Za-z0-9_-]+)\/jobs\/([A-Za-z0-9_-]+)\/([a-z]+)$/,
    );
    if (repoJobMatch) {
      const [, repoId, jobId, action] = repoJobMatch;
      const engine = registry.getEngine(repoId);

      if (!engine) {
        return sendJson(res, 404, { error: `No engine attached for repo: ${repoId}` });
      }

      if (action === "log" && req.method === "GET") {
        const params = new URLSearchParams(url.includes("?") ? url.slice(url.indexOf("?") + 1) : "");
        const since = parseInt(params.get("since") ?? "0", 10);
        registry
          .sendCommand(repoId, { type: "getJobDetail", jobId, id: randomBytes(8).toString("hex") })
          .then((r) => {
            if (!r || "error" in r) return sendJson(res, 404, { error: `Job not found: ${jobId}` });
            const output = (r.output as string[]) ?? [];
            const length = output.length;
            const safeFrom = Number.isFinite(since) && since >= 0 && since <= length ? since : 0;
            const lines = output.slice(safeFrom);
            const info = r.info as { state?: string } | undefined;
            sendJson(res, 200, { lines, length, state: info?.state ?? "unknown" });
          })
          .catch(() => sendJson(res, 500, { error: "engine request failed" }));
        return;
      }

      if (action === "kill" && req.method === "POST") {
        registry
          .sendCommand(repoId, { type: "killJob", jobId, id: randomBytes(8).toString("hex") })
          .then((r) => {
            if (!r || "error" in r) return sendJson(res, 400, { error: (r as { error?: string } | null)?.error ?? "kill failed" });
            sendJson(res, 200, { ok: true });
          })
          .catch(() => sendJson(res, 500, { error: "engine request failed" }));
        return;
      }

      if (action === "restart" && req.method === "POST") {
        registry
          .sendCommand(repoId, { type: "restartJob", jobId, id: randomBytes(8).toString("hex") })
          .then((r) => {
            if (!r || "error" in r) return sendJson(res, 400, { error: (r as { error?: string } | null)?.error ?? "restart failed" });
            const payload = r as { newJobId?: string; ok?: boolean };
            sendJson(res, 200, { ok: true, newJobId: payload.newJobId });
          })
          .catch(() => sendJson(res, 500, { error: "engine request failed" }));
        return;
      }

      if (action === "remove" && req.method === "POST") {
        registry
          .sendCommand(repoId, { type: "removeJob", jobId, id: randomBytes(8).toString("hex") })
          .then((r) => {
            if (!r || "error" in r) return sendJson(res, 400, { error: (r as { error?: string } | null)?.error ?? "remove failed" });
            sendJson(res, 200, { ok: true });
          })
          .catch(() => sendJson(res, 500, { error: "engine request failed" }));
        return;
      }

      if (action === "nudge" && req.method === "POST") {
        readJsonBody(req)
          .then((body): Promise<Record<string, unknown> | null> | null => {
            const { stepId, message } = body as { stepId?: unknown; message?: unknown };
            if (typeof message !== "string" || !message.trim()) {
              sendJson(res, 400, { error: "message is required" });
              return null;
            }
            if (message.length > 32 * 1024) {
              sendJson(res, 400, { error: "message too large (max 32KB)" });
              return null;
            }
            if (typeof stepId !== "string" || !stepId) {
              sendJson(res, 400, { error: "stepId is required" });
              return null;
            }
            return registry.sendCommand(repoId, {
              type: "nudgeJob",
              id: randomBytes(8).toString("hex"),
              jobId,
              stepId,
              message,
            });
          })
          .then((r) => {
            if (!r) return;
            if (typeof r === "object" && "error" in r) sendJson(res, 400, { error: (r as { error?: string }).error });
            else sendJson(res, 200, { ok: true });
          })
          .catch(() => sendJson(res, 500, { error: "engine request failed" }));
        return;
      }

      if (action === "recovery" && req.method === "POST") {
        readJsonBody(req)
          .then((body): Promise<Record<string, unknown> | null> | null => {
            const { action: recoveryAction, message } = body as { action?: string; message?: string };
            if (!recoveryAction || !["retry", "skip", "abort"].includes(recoveryAction)) {
              sendJson(res, 400, { error: "invalid action" });
              return null;
            }
            return registry.sendCommand(repoId, {
              type: "answerRecovery",
              id: randomBytes(8).toString("hex"),
              jobId,
              action: recoveryAction as "retry" | "skip" | "abort",
              message,
            });
          })
          .then((r) => {
            if (!r) return;
            if (typeof r === "object" && "error" in r) sendJson(res, 400, { error: (r as { error?: string }).error });
            else sendJson(res, 200, { ok: true });
          })
          .catch(() => sendJson(res, 500, { error: "engine request failed" }));
        return;
      }
    }

    // GET /repos/:repoId/chats — list all chats (main + side-chats)
    const chatsGetMatch = pathname.match(/^\/repos\/([A-Za-z0-9_-]+)\/chats$/);
    if (chatsGetMatch && req.method === "GET") {
      const [, repoId] = chatsGetMatch;
      const entry = bridges.get(repoId);
      if (!entry) return sendJson(res, 404, { error: `No engine attached for repo: ${repoId}` });
      const list = [...entry.chats.entries()].map(([chatId, c]) => ({ chatId, kind: c.kind, tool: c.tool }));
      return sendJson(res, 200, list);
    }

    // POST /repos/:repoId/chats — create a new side-chat
    if (chatsGetMatch && req.method === "POST") {
      const [, repoId] = chatsGetMatch;
      const entry = bridges.get(repoId);
      if (!entry) return sendJson(res, 404, { error: `No engine attached for repo: ${repoId}` });
      readJsonBody(req).then((body) => {
        const { tool = "claude" } = body as { tool?: unknown };
        if (tool !== "claude" && tool !== "gemini") {
          return sendJson(res, 400, { error: "tool must be 'claude' or 'gemini'" });
        }
        const clientReqId = randomBytes(8).toString("hex");
        const timeout = setTimeout(() => {
          entry.pendingCreates.delete(clientReqId);
          sendJson(res, 503, { error: "supervisor timeout" });
        }, 10_000);
        entry.pendingCreates.set(clientReqId, {
          resolve: (chatId, chatTool) => {
            clearTimeout(timeout);
            sendJson(res, 200, { chatId, kind: "sidechat", tool: chatTool });
          },
          reject: (error) => {
            clearTimeout(timeout);
            sendJson(res, error.includes("limit") ? 429 : 400, { error });
          },
        });
        entry.bridge.createChat(clientReqId, tool as ToolKind);
      }).catch((err: unknown) => {
        sendJson(res, 400, { error: err instanceof Error ? err.message : String(err) });
      });
      return;
    }

    // DELETE /repos/:repoId/chats/:chatId — close a side-chat
    const chatDeleteMatch = pathname.match(/^\/repos\/([A-Za-z0-9_-]+)\/chats\/([^/]+)$/);
    if (chatDeleteMatch && req.method === "DELETE") {
      const [, repoId, chatId] = chatDeleteMatch;
      if (chatId === "main") return sendJson(res, 400, { error: "cannot close main chat" });
      const entry = bridges.get(repoId);
      if (!entry) return sendJson(res, 404, { error: `No engine attached for repo: ${repoId}` });
      if (!entry.chats.has(chatId)) return sendJson(res, 404, { error: "chat not found" });
      const clientReqId = randomBytes(8).toString("hex");
      const timeout = setTimeout(() => {
        entry.pendingCloses.delete(clientReqId);
        sendJson(res, 503, { error: "supervisor timeout" });
      }, 10_000);
      entry.pendingCloses.set(clientReqId, {
        resolve: () => { clearTimeout(timeout); sendJson(res, 200, { ok: true }); },
        reject: (error) => { clearTimeout(timeout); sendJson(res, 400, { error }); },
      });
      entry.bridge.closeChat(chatId, clientReqId);
      return;
    }

    // Switch the jobs tool for a specific repo's engine.
    const jobToolMatch = pathname.match(/^\/repos\/([A-Za-z0-9_-]+)\/job-tool$/);
    if (jobToolMatch && req.method === "POST") {
      const [, repoId] = jobToolMatch;
      const engine = registry.getEngine(repoId);
      if (!engine) return sendJson(res, 404, { error: `No engine attached for repo: ${repoId}` });

      readJsonBody(req)
        .then((body): Promise<Record<string, unknown> | null> | null => {
          const { tool } = body as { tool?: string };
          if (tool !== "claude" && tool !== "gemini" && tool !== "codex") {
            sendJson(res, 400, { error: "tool must be 'claude', 'gemini', or 'codex'" });
            return null;
          }
          return registry.sendCommand(repoId, {
            type: "setJobTool",
            id: randomBytes(8).toString("hex"),
            tool,
          });
        })
        .then((r) => {
          if (!r) return;
          if (typeof r === "object" && "error" in r) {
            sendJson(res, 400, { error: (r as { error?: string }).error });
          } else {
            const tool = (r as { jobTool?: string }).jobTool;
            if (tool === "claude" || tool === "gemini" || tool === "codex") {
              registry.updateJobTool(repoId, tool);
            }
            sendJson(res, 200, { ok: true });
          }
        })
        .catch(() => sendJson(res, 500, { error: "engine request failed" }));
      return;
    }

    // POST /repos/:repoId/paste-image — save a pasted image to disk, return relpath
    const pasteImageMatch = pathname.match(/^\/repos\/([A-Za-z0-9_-]+)\/paste-image$/);
    if (pasteImageMatch && req.method === "POST") {
      const [, repoId] = pasteImageMatch;
      const engine = registry.getEngine(repoId);
      if (!engine) return sendJson(res, 404, { error: `No engine attached for repo: ${repoId}` });
      const ct = (req.headers["content-type"] ?? "").split(";")[0].trim().toLowerCase();
      const ext = IMAGE_MIME_EXT[ct];
      if (!ext) return sendJson(res, 415, { error: `unsupported image type: ${ct}` });
      readBinaryBody(req, 10 * 1024 * 1024).then((buf) => {
        const ts = new Date().toISOString().replace(/[:.]/g, "-");
        const rand = randomBytes(4).toString("hex");
        const filename = `${ts}-${rand}.${ext}`;
        const pastedDir = join(engine.repoPath, ".sparkflow", "pasted");
        mkdirSync(pastedDir, { recursive: true });
        writeFileSync(join(pastedDir, filename), buf);
        sendJson(res, 200, { relpath: `.sparkflow/pasted/${filename}`, bytes: buf.byteLength });
      }).catch((err: unknown) => {
        const msg = err instanceof Error ? err.message : String(err);
        if (msg === "request body too large") sendJson(res, 413, { error: "image too large (max 10 MiB)" });
        else sendJson(res, 500, { error: msg });
      });
      return;
    }

    // GET /repos/:repoId/pasted/:filename — serve a pasted image by filename
    const getPastedMatch = pathname.match(/^\/repos\/([A-Za-z0-9_-]+)\/pasted\/([^/]+)$/);
    if (getPastedMatch && req.method === "GET") {
      const [, repoId, filename] = getPastedMatch;
      const engine = registry.getEngine(repoId);
      if (!engine) return sendJson(res, 404, { error: `No engine attached for repo: ${repoId}` });
      if (!/^[A-Za-z0-9_.-]+\.(png|jpg|jpeg|gif|webp)$/.test(filename)) {
        return sendJson(res, 400, { error: "invalid filename" });
      }
      const EXT_MIME: Record<string, string> = {
        png: "image/png", jpg: "image/jpeg", jpeg: "image/jpeg",
        gif: "image/gif", webp: "image/webp",
      };
      const ext = filename.slice(filename.lastIndexOf(".") + 1).toLowerCase();
      const contentType = EXT_MIME[ext] ?? "application/octet-stream";
      const pastedDir = join(engine.repoPath, ".sparkflow", "pasted");
      const filePath = join(pastedDir, filename);
      try {
        const resolved = realpathSync(filePath);
        const dirResolved = realpathSync(pastedDir);
        if (!resolved.startsWith(dirResolved + "/") && resolved !== dirResolved) {
          return sendJson(res, 400, { error: "invalid path" });
        }
      } catch {
        return sendJson(res, 404, { error: "not found" });
      }
      try {
        const buf = readFileSync(filePath);
        res.writeHead(200, {
          "content-type": contentType,
          "content-length": String(buf.byteLength),
          "cache-control": "private, max-age=86400",
        });
        res.end(buf);
      } catch {
        sendJson(res, 404, { error: "not found" });
      }
      return;
    }

    // Workflow start for a specific repo
    const startWorkflowMatch = pathname.match(/^\/repos\/([A-Za-z0-9_-]+)\/start$/);
    if (startWorkflowMatch && req.method === "POST") {
      const [, repoId] = startWorkflowMatch;
      const engine = registry.getEngine(repoId);
      if (!engine) return sendJson(res, 404, { error: `No engine attached for repo: ${repoId}` });

      readJsonBody(req)
        .then((body): Promise<Record<string, unknown> | null> | null => {
          const { workflowPath, cwd, plan, planText, slug, description } =
            body as {
              workflowPath?: string;
              cwd?: string;
              plan?: string;
              planText?: string;
              slug?: string;
              description?: string;
            };
          if (!workflowPath) {
            sendJson(res, 400, { error: "workflowPath is required" });
            return null;
          }
          return registry.sendCommand(repoId, {
            type: "startWorkflow",
            id: randomBytes(8).toString("hex"),
            workflowPath,
            cwd,
            plan,
            planText,
            slug,
            description,
          });
        })
        .then((r) => {
          if (!r) return;
          if (typeof r === "object" && "error" in r) sendJson(res, 400, { error: (r as { error?: string }).error });
          else sendJson(res, 200, r);
        })
        .catch(() => sendJson(res, 500, { error: "engine request failed" }));
      return;
    }

    res.writeHead(404, { "content-type": "text/plain" });
    res.end("not found");
  });

  const wss = new WebSocketServer({ noServer: true });

  function pickRepoIdFromUrl(url: string): string | null {
    const idx = url.indexOf("?");
    if (idx === -1) return null;
    const params = new URLSearchParams(url.slice(idx + 1));
    const requested = params.get("repoId");
    if (requested && bridges.has(requested)) return requested;
    // Fall back to the first attached engine with a bridge.
    for (const rid of bridges.keys()) return rid;
    return null;
  }

  server.on("upgrade", (req, socket, head) => {
    const url = req.url ?? "";
    if (url.split("?")[0] !== "/chat" || !authorized(req, token)) {
      (socket as Socket).write("HTTP/1.1 401 Unauthorized\r\n\r\n");
      (socket as Socket).destroy();
      return;
    }
    const repoId = pickRepoIdFromUrl(url);
    if (!repoId) {
      (socket as Socket).write("HTTP/1.1 503 Service Unavailable\r\n\r\n");
      (socket as Socket).destroy();
      return;
    }
    const params = new URLSearchParams(url.includes("?") ? url.slice(url.indexOf("?") + 1) : "");
    const chatId = params.get("chatId") ?? "main";
    wss.handleUpgrade(req, socket as Socket, head, (ws) => {
      wsToRepo.set(ws, repoId);
      wsToChat.set(ws, chatId);
      wss.emit("connection", ws, req);
    });
  });

  wss.on("connection", (ws: WebSocket) => {
    const repoId = wsToRepo.get(ws);
    const chatId = wsToChat.get(ws) ?? "main";
    if (!repoId) {
      try { ws.close(1011, "no repo"); } catch { /* ignore */ }
      return;
    }
    const entry = bridges.get(repoId);
    if (!entry) {
      try { ws.close(1011, "no bridge"); } catch { /* ignore */ }
      return;
    }
    const chat = entry.chats.get(chatId);
    if (!chat) {
      try { ws.close(1008, "unknown chatId"); } catch { /* ignore */ }
      return;
    }
    chat.clients.add(ws);
    if (chat.ring.length > 0) {
      ws.send(JSON.stringify({ type: "data", bytes: chat.ring.toString("base64") }));
    }
    if (chatId === "main") {
      const tool = registry.getEngine(repoId)?.chatTool;
      if (tool) ws.send(JSON.stringify({ type: "chat_tool", tool }));
    }

    ws.on("message", (raw) => {
      let parsed: unknown;
      try { parsed = JSON.parse(raw.toString("utf-8")); } catch { return; }
      const msg = parsed as { type?: string; bytes?: string; cols?: number; rows?: number; tool?: string };
      const current = bridges.get(repoId);
      if (!current) return;
      if (msg.type === "data" && typeof msg.bytes === "string") {
        try { current.bridge.writeTo(chatId, Buffer.from(msg.bytes, "base64")); } catch { /* ignore */ }
      } else if (msg.type === "resize" && Number.isFinite(msg.cols) && Number.isFinite(msg.rows)) {
        try { current.bridge.resizeTo(chatId, Math.max(1, msg.cols!), Math.max(1, msg.rows!)); } catch { /* ignore */ }
      } else if (msg.type === "set_chat_tool" && (msg.tool === "claude" || msg.tool === "gemini") && chatId === "main") {
        try { current.bridge.setChatTool(msg.tool); } catch { /* ignore */ }
      }
    });

    ws.on("close", () => {
      const e = bridges.get(repoId);
      e?.chats.get(chatId)?.clients.delete(ws);
    });
  });

  await new Promise<void>((resolve) => server.listen(opts.port, "127.0.0.1", () => resolve()));

  const address = server.address();
  const boundPort = typeof address === "object" && address ? address.port : opts.port;

  return {
    port: boundPort,
    ipcSocketPath,
    registry,
    close: async () => {
      await registry.close();
      await new Promise<void>((resolve) => server.close(() => resolve()));
    },
  };
}

// ---------------------------------------------------------------------------
// Process entry point
// ---------------------------------------------------------------------------

async function main(): Promise<void> {
  const argv = process.argv.slice(2);
  if (argv.length < 2) {
    console.error("Usage: frontend-daemon <ipc-socket-path> <port>");
    process.exit(1);
  }
  const [ipcSocketPath, portStr] = argv;
  const port = parseInt(portStr, 10);
  if (Number.isNaN(port) || port < 0) {
    console.error(`Invalid port: ${portStr}`);
    process.exit(1);
  }

  const token = process.env.SPARKFLOW_WEB_TOKEN;
  if (!token || !/^[0-9a-f]{64}$/.test(token)) {
    console.error("[sparkflow frontend] missing or malformed SPARKFLOW_WEB_TOKEN");
    process.exit(1);
  }

  const handle = await createFrontendDaemon({ ipcSocketPath, port, token });

  process.stderr.write(
    `\n[sparkflow web] ready at http://127.0.0.1:${handle.port}/?token=${token}\n` +
      `[sparkflow web] press Ctrl-C to quit\n\n`,
  );

  let exiting = false;
  async function shutdown(): Promise<void> {
    if (exiting) return;
    exiting = true;
    await handle.close();
    process.exit(0);
  }

  process.on("SIGINT", () => void shutdown());
  process.on("SIGTERM", () => void shutdown());
  process.on("SIGHUP", () => void shutdown());
}

// Only run main() when this file is the direct entry point, not when imported.
if (process.argv[1] && fileURLToPath(import.meta.url) === resolve(process.argv[1])) {
  main().catch((err) => {
    console.error("[sparkflow frontend] fatal:", err);
    process.exit(1);
  });
}
