#!/usr/bin/env node

/**
 * Frontend daemon — the shared HTTP/SSE/WS server for multi-repo sparkflow.
 *
 * This process is spawned (detached) by the first `sparkflow --web` invocation
 * in any repo. Subsequent invocations find it via ~/.sparkflow/dashboard.json
 * and attach their engine daemons to it via the IPC socket.
 *
 * The process can also be started directly for testing by importing
 * `createFrontendDaemon` and calling it with custom options.
 *
 * Invocation (via web/index.ts --frontend-only):
 *   node frontend-daemon.js <ipc-socket-path> <port>
 *
 * Environment:
 *   SPARKFLOW_WEB_TOKEN — auth token (generated by the caller, 64 hex chars)
 */

